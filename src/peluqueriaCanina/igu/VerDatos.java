    private void cargarTabla() {
        // Definir el modelo de la tabla
        DefaultTableModel tablaModelo = new DefaultTableModel() {
            
            //Desabilita la edicion de las filas y columnas
            @Override
            public boolean isCellEditable (int row, int column) {
                return false;
            }
        };
        
        // Establecer nombre de las columnas
        String titulos[] = {"Num", "Nombre", "Color", "Raza", "Alergico",
            "Atencion Especial", "Propietario", "Contacto"};
        tablaModelo.setColumnIdentifiers(titulos);
        
        // Carga de datos desde base de datos
        List<Mascota> listaMascotas = control.traerMascotas();
        
        // Recorrer la lista y mostrar cada elemento en la tabla
        if(listaMascotas != null) {
            for(Mascota mascota : listaMascotas)  {
                Object[] objeto = {
                    mascota.getNumCliente(),
                    mascota.getNombre(),
                    mascota.getColor(),
                    mascota.getRaza(),
                    mascota.getAlergico(),
                    mascota.getAtencionEspecial(),
                    mascota.getUnPropietario().getNombre(),
                    mascota.getUnPropietario().getContacto()
                };
                tablaModelo.addRow(objeto);
            }
        }
        
        tablaMascotas.setModel(tablaModelo);
    }
