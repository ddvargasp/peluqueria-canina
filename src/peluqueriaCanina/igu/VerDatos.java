    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // Controlo que la tabla no este vacia
        if(tablaMascotas.getRowCount() > 0) {
            //Controlo que se haya seleccionado un registro
            if(tablaMascotas.getSelectedRow() != -1) {
                //Obtengo id de la mascota
                int num_cliente  = Integer.parseInt(
                        String.valueOf(
                                tablaMascotas.getValueAt(tablaMascotas.getSelectedRow(), 0)
                        )
                );
                //Llama al metodo borrar
                control.borrarMascota(num_cliente);
                
                //Aviso al usuario que borro correctamente
                mostrarMensaje("Mascota eliminada correctamente", "Informacion", "Borrado de Mascota");
                cargarTabla();
            }
            else {
                mostrarMensaje("No seleccionó ninguna mascota", "Error", "Error al eliminar");
            }
        }
        else {
            mostrarMensaje("No hay nada para eliminar en la tabla", "Error", "Error al eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if(tablaMascotas.getRowCount() > 0) {
            //Controlo que se haya seleccionado un registro
            if(tablaMascotas.getSelectedRow() != -1) {
                //Obtengo id de la mascota
               int num_cliente  = Integer.parseInt(
                        String.valueOf(
                                tablaMascotas.getValueAt(tablaMascotas.getSelectedRow(), 0))
               );
               
                ModificarDatos pantallaModificar = new ModificarDatos(num_cliente);
                pantallaModificar.setVisible(true);
                pantallaModificar.setLocationRelativeTo(null);
                
                this.dispose();
            }
            else {
                mostrarMensaje("No seleccionó ninguna mascota", "Error", "Error al eliminar");
            }
        }
        else {
            mostrarMensaje("No hay nada para eliminar en la tabla", "Error", "Error al eliminar");
        }        
    }//GEN-LAST:event_btnEditarActionPerformed

    public void mostrarMensaje(String mensaje, String tipo, String titulo) {
        JOptionPane optionPane = new JOptionPane(mensaje);
        if(tipo.equals("Informacion")){
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        }
        else if(tipo.equals("Error")) {
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);  //Ubicacion del mensaje
        dialog.setVisible(true);
    }

